// Value types (integer, float, string, boolean)

package main

import "fmt"

func main() {

	// integer
	fmt.Println("1+1 =", 1+1)

	// float value
	fmt.Println("7.0/3.0 =", 7.0/3.0)

	// string value
	fmt.Println("go" + "lang")

	// boolean
	fmt.Println(true && false)
	fmt.Println(true || false)
	fmt.Println(!true)
}
